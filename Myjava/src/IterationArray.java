public class IterationArray {
    public static void main(String[] args){
        int[] num = {3,9,23};
        for (int i=0;i<num.length;i++){
            System.out.println(num[i]);
        }
        String[] animals = {"cat","dog","elephant"};
        for(String animal:animals) {
        System.out.println(animal);
        }
        }
    }
